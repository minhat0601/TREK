-- 2. Trips & Members
-- -----------------------------------------------------------------------------
create table public.trips (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  title text not null,
  description text,
  start_date date,
  end_date date,
  currency text default 'EUR' not null,
  cover_image text,
  is_archived boolean default false not null,
  reminder_days integer default 3 not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table public.trip_members (
  id bigint generated by default as identity primary key,
  trip_id bigint references public.trips(id) on delete cascade not null,
  user_id uuid references auth.users on delete cascade not null,
  invited_by uuid references auth.users,
  added_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(trip_id, user_id)
);

create or replace function public.can_access_trip(trip_id bigint, user_uuid uuid)
returns boolean security definer as $$
begin
  return exists (
    select 1 from public.trips t
    where t.id = can_access_trip.trip_id and t.user_id = can_access_trip.user_uuid
  ) or exists (
    select 1 from public.trip_members tm
    where tm.trip_id = can_access_trip.trip_id and tm.user_id = can_access_trip.user_uuid
  );
end;
$$ language plpgsql;

