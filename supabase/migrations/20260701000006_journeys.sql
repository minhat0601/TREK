-- ── Journeys ──────────────────────────────────────────────────────────────────

-- Owner: full CRUD (mirrors trips policy "Users can delete trips they own")
create policy "Owner can manage journeys"
  on public.journeys for all
  using (auth.role() = 'authenticated' and auth.uid() = user_id);

-- Contributors: read only (mirrors trip_members read access pattern)
create policy "Contributors can read journeys"
  on public.journeys for select
  using (
    auth.role() = 'authenticated' and
    exists (
      select 1 from public.journey_contributors
      where journey_id = id and user_id = auth.uid()
    )
  );

-- ── Journey Entries ───────────────────────────────────────────────────────────

-- Journey owner: manage ALL entries (mirrors "trip owner manages all trip content")
create policy "Journey owner can manage all entries"
  on public.journey_entries for all
  using (
    auth.role() = 'authenticated' and
    exists (select 1 from public.journeys where id = journey_id and user_id = auth.uid())
  );

-- Entry author: manage own entries
create policy "Author can manage own entries"
  on public.journey_entries for all
  using (auth.role() = 'authenticated' and auth.uid() = author_id);

-- All journey members: read entries
create policy "Journey members can read entries"
  on public.journey_entries for select
  using (
    auth.role() = 'authenticated' and
    public.can_access_journey(journey_id, auth.uid())
  );

-- ── Journey Trips ─────────────────────────────────────────────────────────────

create policy "Journey members can read journey_trips"
  on public.journey_trips for select
  using (auth.role() = 'authenticated' and public.can_access_journey(journey_id, auth.uid()));

create policy "Journey owner can manage journey_trips"
  on public.journey_trips for all
  using (
    auth.role() = 'authenticated' and
    exists (select 1 from public.journeys where id = journey_id and user_id = auth.uid())
  );

-- ── Journey Contributors ──────────────────────────────────────────────────────

-- Journey owner: full control over contributor list
create policy "Journey owner can manage contributors"
  on public.journey_contributors for all
  using (
    auth.role() = 'authenticated' and
    exists (select 1 from public.journeys where id = journey_id and user_id = auth.uid())
  );

-- All journey members: see who else is in the journey
create policy "Journey members can read contributor list"
  on public.journey_contributors for select
  using (
    auth.role() = 'authenticated' and
    public.can_access_journey(journey_id, auth.uid())
  );

-- ── Journey Share Links ───────────────────────────────────────────────────────

-- Owner only: manage share links
create policy "Journey owner can manage share links"
  on public.journey_share_links for all
  using (
    auth.role() = 'authenticated' and
    exists (select 1 from public.journeys where id = journey_id and user_id = auth.uid())
  );

-- Authenticated users can read share links (API filters by token before returning to public)
-- Removed using(true) — was exposing entire table to anon users
create policy "Authenticated users can read share links"
  on public.journey_share_links for select
  using (auth.role() = 'authenticated');

-- ── Journey Photos ────────────────────────────────────────────────────────────

-- Journey owner: full CRUD
create policy "Journey owner can manage photos"
  on public.journey_photos for all
  using (
    auth.role() = 'authenticated' and
    exists (select 1 from public.journeys where id = journey_id and user_id = auth.uid())
  );

-- Editors: can also manage photos
create policy "Journey editors can manage photos"
  on public.journey_photos for all
  using (
    auth.role() = 'authenticated' and
    exists (
      select 1 from public.journey_contributors jc
      where jc.journey_id = journey_photos.journey_id
        and jc.user_id = auth.uid()
        and jc.role = 'editor'
    )
  );

-- All journey members: read photos
create policy "Journey members can read photos"
  on public.journey_photos for select
  using (
    auth.role() = 'authenticated' and
    public.can_access_journey(journey_id, auth.uid())
  );

