-- Journeys (travel diary)
create table if not exists public.journeys (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  title text not null,
  subtitle text,
  cover_gradient text,
  cover_image text,
  status text default 'active' not null check(status in ('draft','active','completed','archived')),
  hide_skeletons boolean default false not null,
  created_at timestamp with time zone default timezone('utc', now()) not null,
  updated_at timestamp with time zone default timezone('utc', now()) not null
);

-- Journey entries (individual diary entries)
create table if not exists public.journey_entries (
  id bigint generated by default as identity primary key,
  journey_id bigint references public.journeys(id) on delete cascade not null,
  source_trip_id bigint references public.trips(id) on delete set null,
  source_place_id bigint references public.places(id) on delete set null,
  author_id uuid references auth.users on delete cascade not null,
  type text default 'entry' not null check(type in ('entry','checkin','skeleton')),
  title text,
  story text,
  entry_date date not null,
  entry_time time,
  location_name text,
  location_lat numeric,
  location_lng numeric,
  mood text,
  weather text,
  tags text[],
  pros_cons jsonb,
  visibility text default 'private' not null,
  sort_order integer default 0 not null,
  created_at timestamp with time zone default timezone('utc', now()) not null,
  updated_at timestamp with time zone default timezone('utc', now()) not null
);

-- Journey trips (linked trips to a journey)
create table if not exists public.journey_trips (
  id bigint generated by default as identity primary key,
  journey_id bigint references public.journeys(id) on delete cascade not null,
  trip_id bigint references public.trips(id) on delete cascade not null,
  added_at timestamp with time zone default timezone('utc', now()) not null,
  unique(journey_id, trip_id)
);

-- Journey contributors (shared editing)
create table if not exists public.journey_contributors (
  id bigint generated by default as identity primary key,
  journey_id bigint references public.journeys(id) on delete cascade not null,
  user_id uuid references auth.users on delete cascade not null,
  role text default 'viewer' not null check(role in ('owner','editor','viewer')),
  added_at timestamp with time zone default timezone('utc', now()) not null,
  unique(journey_id, user_id)
);

-- Journey share links (token-based public sharing)
create table if not exists public.journey_share_links (
  id bigint generated by default as identity primary key,
  journey_id bigint references public.journeys(id) on delete cascade not null,
  token text unique not null default encode(gen_random_bytes(24), 'hex'),
  share_timeline boolean default true not null,
  share_gallery boolean default true not null,
  share_map boolean default true not null,
  created_at timestamp with time zone default timezone('utc', now()) not null,
  unique(journey_id)
);

-- Journey photos — dedicated table, NOT sharing trip_files
-- (trip_files.trip_id FK would be violated with trip_id=0)
create table if not exists public.journey_photos (
  id bigint generated by default as identity primary key,
  journey_id bigint references public.journeys(id) on delete cascade not null,
  entry_id bigint references public.journey_entries(id) on delete cascade,
  storage_path text,          -- Supabase Storage path (bucket: journey-photos)
  url text,                   -- Public URL cached here
  original_name text,
  mime_type text default 'image/jpeg' not null,
  file_size bigint,
  caption text,
  sort_order integer default 0 not null,
  shared boolean default false not null,
  created_at timestamp with time zone default timezone('utc', now()) not null
);

-- =============================================================================
-- Helper function: can user access this journey?
-- security definer — same pattern as public.can_access_trip() in init.sql
-- avoids RLS recursion on journey_contributors
-- =============================================================================
create or replace function public.can_access_journey(journey_id bigint, user_uuid uuid)
returns boolean security definer as $$
begin
  return exists (
    select 1 from public.journeys j
    where j.id = can_access_journey.journey_id and j.user_id = can_access_journey.user_uuid
  ) or exists (
    select 1 from public.journey_contributors jc
    where jc.journey_id = can_access_journey.journey_id and jc.user_id = can_access_journey.user_uuid
  );
end;
$$ language plpgsql;

